//como traigo por teclado valores por code javascript?

// Solicita al usuario que ingrese su nombre
let nombre = prompt("Por favor, ingresa tu nombre:");

// Muestra el nombre ingresado en la consola
console.log("Hola, " + nombre + "!");

// Si deseas convertir el valor ingresado a un tipo específico (por ejemplo, número), puedes hacerlo de la siguiente manera:
let edad = parseInt(prompt("Por favor, ingresa tu edad:"));
console.log("Tu edad es: " + edad);